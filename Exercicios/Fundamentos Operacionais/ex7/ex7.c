#include <stdio.h>

int main(void)
{
    // Inicia o contador
    int i = 1;

    while (i <= 100) // -- Enquanto a var i for menor ou igual a 100
    {
        printf("%i\n", i); // -- Imprime o numero

        i++; // -- Adiciona mais um ao contador
    }

    return 0;
}
